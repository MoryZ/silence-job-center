    public void setIdempotentId(String idempotentId) {
        this.idempotentId = idempotentId;
    }

    public String getUniqueId() {
        return uniqueId;
    }

    public void setUniqueId(String uniqueId) {
        this.uniqueId = uniqueId;
    }
